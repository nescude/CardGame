    private void firstCardTextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_firstCardTextMouseClicked
        if (GameMaster.getEndFlag()!=1 && gm.getTurn() == 0){
            if (!this.firstCardText.getText().equals("")) {
                try {
                    gm.pickCard(1);
                    this.firstCardText.setText("");
                } catch (NoManaException e) {
                    JOptionPane.showMessageDialog(null, "No enough mana","Action not available",JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_firstCardTextMouseClicked

